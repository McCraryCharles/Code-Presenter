	<h2 id="getting-started">Getting Started</h2>
	<hr>
	<h3 id="room-host">Room Host</h3>
	<p><strong>The instructor's view - Intended to be displayed on a projector or similar "front of house system"</strong>
	</p>
	<ol>
		<li>From <a href=".">CodePresenter.net</a> Click "Create Room" to generate a room code and load the host interface</li>
		<li>Instruct users to enter the displayed room code into the CodePresenter website to join the room</li>
		<li>User submissions will populate the submissions list, select a submission to display and interact with it</li>
	</ol>
	<h3 id="user"></a>User</h3>
	<p><strong>The audience interface - Intended for writing and uploading of code example is response to the host</strong>
	</p>
	<ol>
		<li>Enter the room code in the CodePresenter website and click "Join Room"</li>
		<li>Enter your first and last name, and click "Get Coding"</li>
		<li>To create a new code example click "New File"</li>
		<li>Click "Publish" to publish your code to the room for display on the host interface</li>
	</ol>
	<br>
	<h2 id="motivation">Motivation</h2>
	<hr>
	<p>As technology and the workplace evolve, so should education and the educational tools used by instructors in the classroom. This project is intended to serve as a replacement for the white board in a computer science classroom.</p>
	<p>CodePresenter was designed to provide a painless way for instructors to engage their students in collaborative instruction. Through rapid application of concepts, students obtain direct feedback to their code, while instructors are able to gauge concept
		absorption of the class.</p>
